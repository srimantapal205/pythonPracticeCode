#A python program to create a Teacher class 
class Teachers:
    def setId(self, id):
        self.id = id
    
    def getId(self):
        return self.id

    def setName(self, name):
        self.name=name
    
    def getName(self):
        return self.name
    
    def setAddress(self, address):
        self.address = address
    
    def getAddress(self):
        return self.address
    
    def setSalary(self, salary):
        self.salary = salary
    
    def getSalary(self):
        return self.salary
