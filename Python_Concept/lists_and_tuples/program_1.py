#A python program to create lists with diffrents type of element
#a general way to create a list
#Create a list with integer number
num =[10,20,30,40,50,60]
print('Total list == :: ', num) #display total list
print('First :: %d, last :: %d' %(num[0], num[5])) #display first and last element

#Create a list with string
names = ['Raju', 'Vani', 'Gopal', 'Laxmi']
